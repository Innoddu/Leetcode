# 45. Jump Game II
def jump(nums):
        cnt = 0
        l = r = 0

        while r < len(nums) - 1:
            farthest = 0
            for i in range(l, r + 1):
                farthest = max(farthest, i + nums[i])
            l = r + 1
            r = farthest
            cnt += 1
        return cnt
